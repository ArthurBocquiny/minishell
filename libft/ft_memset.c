#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*cpy;
	size_t	i;

	i = 0;
	cpy = s;
	while (i < n)
	{
		cpy[i] = c;
		i++;
	}
	return (cpy);
}
